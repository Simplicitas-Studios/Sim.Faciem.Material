namespace Sim.Faciem.Controls
{
    /// <summary>
    /// Mirrors Angular Material's button appearance variants.
    /// </summary>
    public enum MatButtonVariant
    {
        /// <summary>Text-only button with no background or border.</summary>
        Basic,

        /// <summary>Filled button with elevation shadow.</summary>
        Raised,

        /// <summary>Transparent button with a visible border.</summary>
        Stroked,

        /// <summary>Filled button without elevation shadow.</summary>
        Flat,

        /// <summary>Circular icon-sized button with no background.</summary>
        Icon,

        /// <summary>Large circular floating action button.</summary>
        Fab,

        /// <summary>Smaller circular floating action button.</summary>
        MiniFab,
    }
}

