namespace Sim.Faciem.Controls
{
    /// <summary>
    /// Mirrors Angular Material's button color palette inputs.
    /// </summary>
    public enum MatButtonColor
    {
        /// <summary>No theme color — inherits surface/on-surface from the active theme.</summary>
        Default,

        /// <summary>Applies the primary palette color to the button.</summary>
        Primary,

        /// <summary>Applies the accent palette color to the button.</summary>
        Accent,

        /// <summary>Applies the warn palette color to the button.</summary>
        Warn,
    }
}

